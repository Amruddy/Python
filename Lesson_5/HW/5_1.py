# Напишите функцию f, которая на вход принимает два числа a и b, 
# и возводит число a в целую степень b с помощью рекурсии.

def power(a, b):
    # Базовый случай: если степень равна 0, возвращаем 1
    if b == 0:
        return 1
    # Рекурсивный случай: умножаем число a на результат возведения в степень b-1
    else:
        return a * power(a, b - 1)

# Пример использования функции:
a = 2
b = 3
result = power(a, b)
print(f"{a} в степени {b} равно {result}")